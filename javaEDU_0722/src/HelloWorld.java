// class(생성자) : 변수(필드), 메서드(함수) 등을 정의할 수 있음
public class HelloWorld {
    // 클래스내 지역변수
    // 메인 메서드
    public static void main(String[] args) {
        // 변수(필드) 선언
        String strVal = "Hello";
        int numVal = 5;
        // 메서드 호출 (System.out.println);ssss
        System.out.println("소녀시대");
        System.out.println(strVal);
        System.out.println(numVal);
    }
}
